import { useEffect, useRef } from 'react';
import * as Notifications from 'expo-notifications';
import useToast from '@/hooks/useToast';

export default function NotificationListener() {
  const { showSuccess } = useToast();
  const notificationListener = useRef<Notifications.Subscription>();
  const responseListener = useRef<Notifications.Subscription>();

  useEffect(() => {
    // Notificaci贸n recibida (en foreground)
    notificationListener.current = Notifications.addNotificationReceivedListener(notification => {
      console.log('[ Notificaci贸n recibida]', notification);

      const { title, body } = notification.request.content;
      showSuccess({
        title: title || 'Notificaci贸n',
        message: body || '',
      });
    });

    // Usuario toc贸 la notificaci贸n
    responseListener.current = Notifications.addNotificationResponseReceivedListener(response => {
      console.log('[ Notificaci贸n tocada]', response);
      const data = response.notification.request.content.data;
      console.log('[ Data de notificaci贸n]', data);

      // Aqu铆 puedes redirigir seg煤n `data.route` o `data.params`
    });

    return () => {
      if (notificationListener.current)
        Notifications.removeNotificationSubscription(notificationListener.current);
      if (responseListener.current)
        Notifications.removeNotificationSubscription(responseListener.current);
    };
  }, []);

  return null;
}
